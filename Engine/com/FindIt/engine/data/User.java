package com.FindIt.engine.data;

public class User 
{
	private String number;
	private String IMEI;
	private String enPswd;
	private String enPswdHint;
	private String dePswd;
	private String dePswdHint;
	private String ownerName;
	private String alternateNumber;
	
	public User()
	{
		setNumber(null);
		setIMEI(null);
		setEnPswd(null);
		setEnPswdHint(null);
		setOwnerName(null);
		setAlternateNumber(null);
	}
	
	/**
	 * @param number the number to set
	 */
	public void setNumber(String number) {
		this.number = number;
	}
	/**
	 * @return the number
	 */
	public String getNumber() {
		return number;
	}
	/**
	 * @param iMEI the iMEI to set
	 */
	public void setIMEI(String iMEI) {
		IMEI = iMEI;
	}
	/**
	 * @return the iMEI
	 */
	public String getIMEI() {
		return IMEI;
	}

	/**
	 * @param enPswd the enPswd to set
	 */
	public void setEnPswd(String enPswd) {
		this.enPswd = enPswd;
	}

	/**
	 * @return the enPswd
	 */
	public String getEnPswd() {
		return enPswd;
	}

	/**
	 * @param enPswdHint the enPswdHint to set
	 */
	public void setEnPswdHint(String enPswdHint) {
		this.enPswdHint = enPswdHint;
	}

	/**
	 * @return the enPswdHint
	 */
	public String getEnPswdHint() {
		return enPswdHint;
	}

	/**
	 * @param dePswd the dePswd to set
	 */
	public void setDePswd(String dePswd) {
		this.dePswd = dePswd;
	}

	/**
	 * @return the dePswd
	 */
	public String getDePswd() {
		return dePswd;
	}

	/**
	 * @param dePswdHint the dePswdHint to set
	 */
	public void setDePswdHint(String dePswdHint) {
		this.dePswdHint = dePswdHint;
	}

	/**
	 * @return the dePswdHint
	 */
	public String getDePswdHint() {
		return dePswdHint;
	}

	/**
	 * @param ownerName the ownerName to set
	 */
	public void setOwnerName(String ownerName) {
		this.ownerName = ownerName;
	}

	/**
	 * @return the ownerName
	 */
	public String getOwnerName() {
		return ownerName;
	}

	/**
	 * @param alternateNumber the alternateNumber to set
	 */
	public void setAlternateNumber(String alternateNumber) {
		this.alternateNumber = alternateNumber;
	}

	/**
	 * @return the alternateNumber
	 */
	public String getAlternateNumber() {
		return alternateNumber;
	}
}
